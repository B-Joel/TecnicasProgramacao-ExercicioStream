package model;

public class OscarJovemPremiado {
    private int idade;
    private String nome;

    public OscarJovemPremiado(int idade, String nome) {
        this.idade = idade;
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public String getNome() {
        return nome;
    }
}
